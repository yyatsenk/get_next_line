#include "libft.h"

void	ft_whiteskip(const char *str, int *i)
{
	while (str[*i] == '\t' || str[*i] == '\n' || str[*i] == '\v'
	|| str[*i] == '\f' || str[*i] == '\r' || str[*i] == ' ')
		*i = *i + 1;
}
